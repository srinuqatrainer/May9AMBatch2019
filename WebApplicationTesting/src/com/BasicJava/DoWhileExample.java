package com.BasicJava;

public class DoWhileExample {

	public static void main(String[] args) {
		
		int k=10;
		
		do
		{
			System.out.println(" LiveTech ");
			k=k+2;
			
		}while(k<=4);
		
		
		System.out.println(" **************** ");
		
		int p=5;
		
		do
		{
			System.out.println(" Testing ");
			p--;
			
		} while(p>=6);
		

	}

}
