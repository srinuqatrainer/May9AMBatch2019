package com.BasicJava;

public class VariableExample {

	public static void main(String[] args) {
		
		// DataType variable = Assigned value
		
		int a=10;
		a=30;
		
		String b="hello";
		
		double c=10.123;
		
		char d='M';
		//char d='10';
		
		String p="10";
		
		System.out.println("The interger value of a is :" + a);
		System.out.println("The String value of b is : " + b);
		System.out.println("The double value of c is : " + c);
		System.out.println("The character value of d is : " +d);
		System.out.println(" The String value of p is : "+ p);
		
		// A variable should be assigned with a Value
		// A variable should be declared with a data Type
		// A variable can save only one value at a time
		// A variable once declared with a data type, cannot be saved with other dataType value
		// A variable once declared with a data type need not be declared again in the same method  
	}

}
