package com.BasicJava;

public class NestedIfCondition {

	public static void main(String[] args) {
		
		int a=17;
		int b=12;
		
		if(a>b)
		{
			if(a == b)
			{
				System.out.println("Hii");
			}
			else
			{
				System.out.println("Hello");
			}
		}
		else
		{
			System.out.println("Bye");
		}

	}

}
